package controller;



public class Onibus {

	private int id;
	private String txtPlaca;
	private String txtDesc;
	private String txtModelo;
	private String txtKmRodado;
	private String txtKmManutencao;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTxtPlaca() {
		return txtPlaca;
	}
	public void setTxtPlaca(String txtPlaca) {
		this.txtPlaca = txtPlaca;
	}
	public String getTxtDesc() {
		return txtDesc;
	}
	public void setTxtDesc(String txtDesc) {
		this.txtDesc = txtDesc;
	}
	public String getTxtModelo() {
		return txtModelo;
	}
	public void setTxtModelo(String txtModelo) {
		this.txtModelo = txtModelo;
	}
	public String getTxtKmRodado() {
		return txtKmRodado;
	}
	public void setTxtKmRodado(String txtKmRodado) {
		this.txtKmRodado = txtKmRodado;
	}
	public String getTxtKmManutencao() {
		return txtKmManutencao;
	}
	public void setTxtKmManutencao(String txtKmManutencao) {
		this.txtKmManutencao = txtKmManutencao;
	}


}
